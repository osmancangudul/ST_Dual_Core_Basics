	rpmsg_init_ept(&rp_endpoint, RPMSG_CHAN_NAME, RPMSG_ADDR_ANY,
			RPMSG_ADDR_ANY, NULL, NULL);

	/* OpenAmp ve Libmetal kütüphanelerini hazırlıyoruz. */
	if (MX_OPENAMP_Init(RPMSG_MASTER, new_service_cb) != HAL_OK) {
		Error_Handler();
	}

